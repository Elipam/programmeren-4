public class Vakken {
    public String vakcode;
    public float cijfer;

    public Vakken(String vakcode, float cijfer) {
        this.vakcode = vakcode;
        this.cijfer = cijfer;
    }
    public String getVakcode() {
        return vakcode;
    }
    public double getCijfer() {
        return cijfer;
    }
}
